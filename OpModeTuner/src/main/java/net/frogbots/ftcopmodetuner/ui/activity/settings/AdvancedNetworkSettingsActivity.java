package net.frogbots.ftcopmodetuner.ui.activity.settings;

import android.os.Bundle;
import android.preference.PreferenceFragment;

import net.frogbots.ftcopmodetuner.R;

/**
 * SettingsActivity for the Advanced Network screen
 */

public class AdvancedNetworkSettingsActivity extends BaseSettingsActivity
{
    @Override
    protected void onCreate(Bundle savedInstanceState)
    {
        super.onCreate(savedInstanceState);
        getFragmentManager().beginTransaction().replace(android.R.id.content, new MyPreferenceFragment()).commit();
        getSupportActionBar().setTitle(R.string.advanced_network_settings_activity_title);
    }

    public static class MyPreferenceFragment extends PreferenceFragment
    {
        @Override
        public void onCreate(final Bundle savedInstanceState)
        {
            super.onCreate(savedInstanceState);
            addPreferencesFromResource(R.xml.advanced_network_preferences);
        }
    }
}